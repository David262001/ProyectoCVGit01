    public void ingresar() {
        try {
            Conexion conexion = new Conexion();
            Connection cc = (Connection) conexion.conectar();
            String nombre, apellido;
            if (cc != null) {
                String sql = "SELECT usuario, clave FROM  usuarios WHERE usuario = ? AND clave = ?";

                if (jtxtUsuario.getText().equals("") || jpssClave.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Inserte Usuario");
                    jtxtUsuario.requestFocus();
                } else {
                    PreparedStatement psd = (PreparedStatement) cc.prepareStatement(sql);
                    psd.setString(1, jtxtUsuario.getText());
                    psd.setString(2, jpssClave.getText());
                    ResultSet resultado = psd.executeQuery();
                    if (resultado.next()) {

                        nombre = resultado.getString("usuario");

                        JOptionPane.showMessageDialog(null, "¡Bienvenido!\n" + nombre);

                        Estudiantes p = new Estudiantes();
                        p.setVisible(true);

                    } else {

                        JOptionPane.showMessageDialog(null, "Fallo de Inicio de Sesión");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Porfavor, levantar el servicio");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Fallido " + ex);
        }
    }

